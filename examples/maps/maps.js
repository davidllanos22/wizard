var playerEntity = function(params){
    this.x = params.x;
    this.y = params.y;
    this.xx = params.xx;
    this.yy = params.yy;
    this.speed = 0.2;

    this.render = function(wiz){
        wiz.drawSprite("tileset", this.x, this.y, 2, 0);
    };

    this.update = function(wiz){
        if(WIZARD.input.keyPressed(WIZARD.keys.LEFT)){
            this.x -= this.speed;
        }else if(WIZARD.input.keyPressed(WIZARD.keys.RIGHT)){
            this.x += this.speed;
        }if(WIZARD.input.keyPressed(WIZARD.keys.UP)){
            this.y -= this.speed;
        }if(WIZARD.input.keyPressed(WIZARD.keys.DOWN)){
            this.y += this.speed;
        }
    };
};

var mapLoader = function(id, x, y, xx, yy, layer, sceneName){
    if(id != -1){
        if(id == 2) { //Player
            WIZARD.entity.instantiateToScene("player", {x: x * 8, y: y * 8}, sceneName);
        }else{
            var scene = WIZARD.scene.scenes[sceneName];
            if(!scene.tiles) scene.tiles = [];
            if(!scene.tiles["layer" + layer]) scene.tiles["layer" + layer] = [];
            var tiles = scene.tiles["layer" + layer];
            WIZARD.entity.instantiateToList("tile", {x: x * 8, y: y * 8, xx, yy}, tiles);
        }

    }
};

